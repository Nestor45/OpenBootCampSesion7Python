def suma(x,y):
    """Funcion que nos permite sumar"""
    return x + y

def resta(x,y):
    """Funcion que nos permite restar"""
    return x - y

def multiplicar(x,y):
    """Funcion que nos permite multiplicar"""
    return x * y

def dividir(x,y):
    """Funcion que nos permite dividir"""
    return x / y